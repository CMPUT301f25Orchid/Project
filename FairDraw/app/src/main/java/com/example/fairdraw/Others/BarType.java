package com.example.fairdraw.Others;

/**
 * Represents the different types of navigation bars used in the FairDraw application.
 * Each bar type corresponds to a different user role and provides role-specific navigation options.
 */
public enum BarType {
    /** Navigation bar for entrants participating in events */
    ENTRANT,
    /** Navigation bar for organizers creating and managing events */
    ORGANIZER,
    /** Navigation bar for administrators managing the application */
    ADMIN
}
